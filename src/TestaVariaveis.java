public class TestaVariaveis { //Convenção de codigo: Classe com a primeira maiuscula.
    public static void main (String[] args){
        System.out.println("Ola novo teste");

        int idade;
        idade = 37; // Variavel começa com minuscula -- Não abreviar

        System.out.println(idade);

        idade = 30 + 10;

        System.out.println(idade);

        idade = 7*5+2;

        System.out.println(idade);

        System.out.println("A idade é " + idade + ", parabens!");

    }
}
